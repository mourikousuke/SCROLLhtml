package jp.ac.tokushima_u.is.ll.entity;

import java.util.Date;

public class JEWord {

	private String jword;
	private String eword;
	private String content;
	private String image;
	
	private String nickname;
	private Date time;
	private String placename;
	private String placeattribute;
	private String lat;
	private String log;
	
	public String getJword() {
		return jword;
	}
	public void setJword(String jword) {
		this.jword = jword;
	}
	public String getEword() {
		return eword;
	}
	public void setEword(String eword) {
		this.eword = eword;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getPlacename() {
		return placename;
	}
	public void setPlacename(String placename) {
		this.placename = placename;
	}
	public String getPlaceattribute() {
		return placeattribute;
	}
	public void setPlaceattribute(String placeattribute) {
		this.placeattribute = placeattribute;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLog() {
		return log;
	}
	public void setLog(String log) {
		this.log = log;
	}
	
	
}
